export class SaaSMakerClient {
  constructor(options = {}) {
    this.baseUrl = (options.baseUrl ?? process.env.SAASMAKER_API_URL ?? 'https://api.sassmaker.com').replace(/\/$/, '');
    this.sessionToken = options.sessionToken ?? process.env.SAASMAKER_SESSION_TOKEN;
    this.fetchImpl = options.fetchImpl ?? fetch;
  }

  async listMarketingPosts(filters = {}) {
    if (!this.sessionToken) {
      throw new Error('missing SAASMAKER_SESSION_TOKEN');
    }

    const url = new URL(`${this.baseUrl}/v1/marketing/posts`);
    for (const [key, value] of Object.entries(filters)) {
      if (value !== undefined && value !== null && value !== '') url.searchParams.set(key, String(value));
    }
    const res = await this.fetchImpl(url, {
      headers: {
        'authorization': `Bearer ${this.sessionToken}`,
      },
    });
    if (!res.ok) throw new Error(`SaaS Maker marketing list failed ${res.status}: ${await res.text()}`);
    const payload = await res.json();
    return payload?.data ?? [];
  }

  async updateMarketingPost(id, patch) {
    if (!this.sessionToken) {
      return {
        skipped: true,
        reason: 'missing SAASMAKER_SESSION_TOKEN',
        patch,
      };
    }

    const res = await this.fetchImpl(`${this.baseUrl}/v1/marketing/posts/${encodeURIComponent(id)}`, {
      method: 'PATCH',
      headers: {
        'authorization': `Bearer ${this.sessionToken}`,
        'content-type': 'application/json',
      },
      body: JSON.stringify(patch),
    });
    if (!res.ok) throw new Error(`SaaS Maker marketing sync failed ${res.status}: ${await res.text()}`);
    return {
      skipped: false,
      data: await res.json(),
    };
  }
}

export function renderPatchForMarketingPost(renderResult) {
  const videoUrl = firstVideoUrl(renderResult);
  const notes = [
    'Reel draft generated by reel-pipeline.',
    `provider: ${renderResult.provider}`,
    `external_task_id: ${renderResult.externalTaskId}`,
    `status: ${renderResult.status}`,
  ].join('\n');
  return {
    asset_url: videoUrl,
    result_url: videoUrl,
    notes,
  };
}

function firstVideoUrl(renderResult) {
  return normalizeUrl(renderResult.videos?.[0] ?? renderResult.combinedVideos?.[0] ?? renderResult.videoUrl ?? null);
}

function normalizeUrl(value) {
  if (typeof value !== 'string' || !value.trim()) return null;
  if (value.startsWith('http://') || value.startsWith('https://') || value.startsWith('file://')) return value;
  if (value.startsWith('/')) return `file://${value}`;
  return `file://${value}`;
}
