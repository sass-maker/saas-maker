const {
  createRunOncePlugin,
  withAppDelegate,
  withInfoPlist,
} = require("expo/config-plugins");

const pluginName = "with-ios-scene-lifecycle";
const pluginVersion = "1.0.0";

const markers = {
  property: "// @generated by with-ios-scene-lifecycle: property",
  launch: "// @generated by with-ios-scene-lifecycle: launch",
  methods: "// @generated by with-ios-scene-lifecycle: methods",
  delegate: "// @generated by with-ios-scene-lifecycle: delegate",
};

const factoryPropertyAnchor =
  "  var reactNativeFactory: RCTReactNativeFactory?\n";

const legacyLaunchBlock = `#if os(iOS) || os(tvOS)
    window = UIWindow(frame: UIScreen.main.bounds)
    factory.startReactNative(
      withModuleName: "main",
      in: window,
      launchOptions: launchOptions)
#endif`;

const linkingAnchor = "  // Linking API\n";
const reactNativeDelegateAnchor = "class ReactNativeDelegate:";

const sceneProperty = `  ${markers.property}
  private var launchOptions: [UIApplication.LaunchOptionsKey: Any]?
`;

const sceneLaunch = `    ${markers.launch}
    self.launchOptions = launchOptions`;

const appDelegateMethods = `  ${markers.methods}
  func connectReactNative(
    to windowScene: UIWindowScene,
    sceneLaunchOptions: [UIApplication.LaunchOptionsKey: Any]
  ) -> UIWindow? {
    if let window {
      window.windowScene = windowScene
      window.makeKeyAndVisible()
      return window
    }

    guard let reactNativeFactory else { return nil }

    var reactNativeLaunchOptions = launchOptions ?? [:]
    for (key, value) in sceneLaunchOptions {
      reactNativeLaunchOptions[key] = value
    }

    let window = UIWindow(windowScene: windowScene)
    self.window = window
    reactNativeFactory.startReactNative(
      withModuleName: "main",
      in: window,
      launchOptions: reactNativeLaunchOptions.isEmpty ? nil : reactNativeLaunchOptions)
    launchOptions = nil
    return window
  }

  public func application(
    _ application: UIApplication,
    configurationForConnecting connectingSceneSession: UISceneSession,
    options: UIScene.ConnectionOptions
  ) -> UISceneConfiguration {
    return UISceneConfiguration(
      name: "Default Configuration",
      sessionRole: connectingSceneSession.role)
  }

`;

const sceneDelegate = `${markers.delegate}
class SceneDelegate: UIResponder, UIWindowSceneDelegate {
  var window: UIWindow?

  private var appDelegate: AppDelegate? {
    UIApplication.shared.delegate as? AppDelegate
  }

  func scene(
    _ scene: UIScene,
    willConnectTo session: UISceneSession,
    options connectionOptions: UIScene.ConnectionOptions
  ) {
    guard
      session.role == .windowApplication,
      let windowScene = scene as? UIWindowScene,
      let appDelegate
    else { return }

    window = appDelegate.connectReactNative(
      to: windowScene,
      sceneLaunchOptions: launchOptions(from: connectionOptions))
  }

  func sceneDidBecomeActive(_ scene: UIScene) {
    appDelegate?.applicationDidBecomeActive(UIApplication.shared)
  }

  func sceneWillResignActive(_ scene: UIScene) {
    appDelegate?.applicationWillResignActive(UIApplication.shared)
  }

  func sceneWillEnterForeground(_ scene: UIScene) {
    appDelegate?.applicationWillEnterForeground(UIApplication.shared)
  }

  func sceneDidEnterBackground(_ scene: UIScene) {
    appDelegate?.applicationDidEnterBackground(UIApplication.shared)
  }

  func scene(_ scene: UIScene, openURLContexts urlContexts: Set<UIOpenURLContext>) {
    guard let appDelegate else { return }
    for context in urlContexts {
      open(context, with: appDelegate)
    }
  }

  func scene(_ scene: UIScene, continue userActivity: NSUserActivity) {
    guard let appDelegate else { return }
    continueActivity(userActivity, with: appDelegate)
  }

  private func launchOptions(
    from connectionOptions: UIScene.ConnectionOptions
  ) -> [UIApplication.LaunchOptionsKey: Any] {
    var launchOptions: [UIApplication.LaunchOptionsKey: Any] = [:]

    if let context = connectionOptions.urlContexts.first {
      launchOptions[
        UIApplication.LaunchOptionsKey(
          rawValue: "UIApplicationLaunchOptionsURLKey")
      ] = context.url
    }

    if let userActivity = connectionOptions.userActivities.first {
      let activityDictionary: [AnyHashable: Any] = [
        UIApplication.LaunchOptionsKey(
          rawValue: "UIApplicationLaunchOptionsUserActivityTypeKey"
        ): userActivity.activityType,
        "UIApplicationLaunchOptionsUserActivityKey": userActivity
      ]
      launchOptions[
        UIApplication.LaunchOptionsKey(
          rawValue: "UIApplicationLaunchOptionsUserActivityDictionaryKey")
      ] = activityDictionary
    }

    return launchOptions
  }

  private func open(_ context: UIOpenURLContext, with appDelegate: AppDelegate) {
    var options: [UIApplication.OpenURLOptionsKey: Any] = [
      .openInPlace: context.options.openInPlace
    ]
    if let sourceApplication = context.options.sourceApplication {
      options[.sourceApplication] = sourceApplication
    }
    if let annotation = context.options.annotation {
      options[.annotation] = annotation
    }

    _ = appDelegate.application(
      UIApplication.shared,
      open: context.url,
      options: options)
  }

  private func continueActivity(
    _ userActivity: NSUserActivity,
    with appDelegate: AppDelegate
  ) {
    _ = appDelegate.application(
      UIApplication.shared,
      continue: userActivity,
      restorationHandler: { _ in })
  }
}

`;

function createSceneManifest() {
  return {
    UIApplicationSupportsMultipleScenes: false,
    UISceneConfigurations: {
      UIWindowSceneSessionRoleApplication: [
        {
          UISceneConfigurationName: "Default Configuration",
          UISceneDelegateClassName: "$(PRODUCT_MODULE_NAME).SceneDelegate",
        },
      ],
    },
  };
}

function applySceneManifest(infoPlist) {
  return {
    ...infoPlist,
    UIApplicationSceneManifest: createSceneManifest(),
  };
}

function transformAppDelegate(contents) {
  const markerCount = Object.values(markers).filter((marker) =>
    contents.includes(marker),
  ).length;

  if (markerCount === Object.keys(markers).length) {
    return contents;
  }
  if (markerCount > 0) {
    throw new Error(
      `${pluginName}: found a partial generated scene lifecycle; regenerate the native project from a clean directory`,
    );
  }

  for (const [label, anchor] of [
    ["React Native factory property", factoryPropertyAnchor],
    ["legacy React Native launch block", legacyLaunchBlock],
    ["linking section", linkingAnchor],
    ["ReactNativeDelegate declaration", reactNativeDelegateAnchor],
  ]) {
    if (!contents.includes(anchor)) {
      throw new Error(
        `${pluginName}: Expo AppDelegate template changed; missing ${label}`,
      );
    }
  }

  let result = contents.replace(
    factoryPropertyAnchor,
    `${factoryPropertyAnchor}\n${sceneProperty}`,
  );
  result = result.replace(legacyLaunchBlock, sceneLaunch);
  result = result.replace(
    linkingAnchor,
    `${appDelegateMethods}${linkingAnchor}`,
  );
  result = result.replace(
    reactNativeDelegateAnchor,
    `${sceneDelegate}${reactNativeDelegateAnchor}`,
  );
  return result;
}

const withIosSceneLifecycle = (config) => {
  config = withInfoPlist(config, (configWithPlist) => {
    configWithPlist.modResults = applySceneManifest(configWithPlist.modResults);
    return configWithPlist;
  });

  config = withAppDelegate(config, (configWithAppDelegate) => {
    if (configWithAppDelegate.modResults.language !== "swift") {
      throw new Error(
        `${pluginName}: expected a Swift AppDelegate, received ${configWithAppDelegate.modResults.language}`,
      );
    }
    configWithAppDelegate.modResults.contents = transformAppDelegate(
      configWithAppDelegate.modResults.contents,
    );
    return configWithAppDelegate;
  });

  return config;
};

const plugin = createRunOncePlugin(
  withIosSceneLifecycle,
  pluginName,
  pluginVersion,
);

plugin.applySceneManifest = applySceneManifest;
plugin.createSceneManifest = createSceneManifest;
plugin.transformAppDelegate = transformAppDelegate;

module.exports = plugin;
