const { createRunOncePlugin, withPodfile } = require("expo/config-plugins");

const pluginName = "with-ios-pod-deployment-target";
const pluginVersion = "1.0.0";
const minimumDeploymentTarget = "16.4";
const marker = "# @generated by with-ios-pod-deployment-target";
const postInstallAnchor = "  post_install do |installer|\n";
const postInstallEnd = "  end\nend\n";

const deploymentTargetFix = `
    ${marker}
    minimum_deployment_target = Gem::Version.new('${minimumDeploymentTarget}')
    installer.pods_project.targets.each do |target|
      target.build_configurations.each do |config|
        deployment_target = config.build_settings['IPHONEOS_DEPLOYMENT_TARGET']
        next if deployment_target &&
          Gem::Version.correct?(deployment_target) &&
          Gem::Version.new(deployment_target) >= minimum_deployment_target

        config.build_settings['IPHONEOS_DEPLOYMENT_TARGET'] = minimum_deployment_target.to_s
      end
    end
`;

function transformPodfile(contents) {
  if (contents.includes(marker)) {
    return contents;
  }

  const postInstallIndex = contents.indexOf(postInstallAnchor);
  if (postInstallIndex < 0) {
    throw new Error(
      `${pluginName}: Expo Podfile template changed; missing post_install block`,
    );
  }

  const endIndex = contents.indexOf(
    postInstallEnd,
    postInstallIndex + postInstallAnchor.length,
  );
  if (endIndex < 0) {
    throw new Error(
      `${pluginName}: Expo Podfile template changed; missing target terminator`,
    );
  }

  return `${contents.slice(0, endIndex)}${deploymentTargetFix}${contents.slice(endIndex)}`;
}

const withIosPodDeploymentTarget = (config) =>
  withPodfile(config, (configWithPodfile) => {
    configWithPodfile.modResults.contents = transformPodfile(
      configWithPodfile.modResults.contents,
    );
    return configWithPodfile;
  });

const plugin = createRunOncePlugin(
  withIosPodDeploymentTarget,
  pluginName,
  pluginVersion,
);

plugin.minimumDeploymentTarget = minimumDeploymentTarget;
plugin.transformPodfile = transformPodfile;

module.exports = plugin;
