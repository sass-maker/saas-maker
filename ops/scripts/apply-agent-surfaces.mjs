#!/usr/bin/env node
/**
 * Apply fleet agent-indexing surfaces across registered products.
 *
 * Usage:
 *   node fleet-ops/scripts/apply-agent-surfaces.mjs
 *   node fleet-ops/scripts/apply-agent-surfaces.mjs --id rolepatch
 *   node fleet-ops/scripts/apply-agent-surfaces.mjs --dry-run
 *   node fleet-ops/scripts/apply-agent-surfaces.mjs --jsonld --dry-run
 */

import {
  existsSync,
  mkdirSync,
  readFileSync,
  writeFileSync,
  copyFileSync,
} from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { loadRegistry, productOrigin } from './lib/registry.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const FLEET_ROOT = resolve(__dirname, '../..');
const REGISTRY = join(FLEET_ROOT, 'fleet-ops/config/agent-surfaces-registry.json');
const EDGE_TEMPLATE = join(
  FLEET_ROOT,
  'fleet-ops/lib/agent-surfaces/agent-edge.template.mjs'
);
const MARKER = 'handleAgentEdge';
const IMPORT_MARKER = "from './agent-edge.mjs'";

// JSON-LD fleet publisher constants
const FLEET_ORG_ID = 'https://sassmaker.com/#org';
const FLEET_ORG_NAME = 'SaaS Maker (Foundry)';
const FLEET_HUB_URL = 'https://sassmaker.com';
const JSONLD_START = '<!-- fleet-jsonld:start (generated by apply-agent-surfaces; edit registry, not this) -->';
const JSONLD_END = '<!-- fleet-jsonld:end -->';

const args = process.argv.slice(2);
const dryRun = args.includes('--dry-run');
const jsonldMode = args.includes('--jsonld');
const jsonldEmitMode = args.includes('--jsonld-emit');
const onlyId = args.includes('--id') ? args[args.indexOf('--id') + 1] : null;

const registry = loadRegistry(REGISTRY);

// --- JSON-LD emit mode: write standalone snippet files to fleet-ops/out/jsonld/ ---
if (jsonldEmitMode) {
  const outDir = join(FLEET_ROOT, 'fleet-ops/out/jsonld');
  if (!dryRun) {
    mkdirSync(outDir, { recursive: true });
  }

  let ok = 0;
  let fail = 0;

  for (const product of registry.products) {
    if (onlyId && product.id !== onlyId) continue;

    // Only emit for products with headFile (skip saas-maker-docs, psi-swarm)
    if (!product.headFile) continue;

    const json = buildJsonLd(product, registry);
    const jsonStr = JSON.stringify(json, null, 2);

    try {
      JSON.parse(jsonStr);
    } catch (e) {
      fail++;
      console.error(`✗ ${product.id}: invalid JSON: ${e.message}`);
      continue;
    }

    const fileName = `${product.id}.json`;
    const content = `${JSONLD_START}\n${jsonStr}\n${JSONLD_END}\n`;

    if (dryRun) {
      ok++;
      console.log(`✓ ${product.id}: would emit ${fileName}`);
      continue;
    }

    writeFileSync(join(outDir, fileName), content, 'utf8');
    ok++;
    console.log(`✓ ${product.id}: emitted ${fileName}`);
  }

  console.log(`\nJSON-LD emit: ${ok} ok, ${fail} fail${dryRun ? ' (dry-run)' : ''}`);
  process.exit(fail === 0 ? 0 : 1);
}

// --- JSON-LD mode: dry-run prints JSON + would-be diff; wet-run injects ---
if (jsonldMode) {
  let ok = 0;
  let fail = 0;
  let skipped = 0;

  for (const product of registry.products) {
    if (onlyId && product.id !== onlyId) continue;

    const json = buildJsonLd(product, registry);
    const jsonStr = JSON.stringify(json);

    // Validate JSON
    try {
      JSON.parse(jsonStr);
    } catch (e) {
      fail++;
      console.error(`✗ ${product.id}: invalid JSON: ${e.message}`);
      continue;
    }

    // Products without headFile: print JSON only (snippet emission = task 7)
    if (!product.headFile) {
      skipped++;
      if (dryRun) {
        console.log(`· ${product.id}: no headFile (snippet — task 7)`);
        console.log(jsonStr);
        console.log('');
      }
      continue;
    }

    // Next.js layouts (.tsx) use JSX dangerouslySetInnerHTML, not text injection.
    // Use --jsonld-emit to generate snippet files, then insert by hand.
    if (product.headFile.endsWith('.tsx') || product.headFile.endsWith('.jsx')) {
      skipped++;
      if (dryRun) {
        console.log(`· ${product.id}: JSX headFile (use --jsonld-emit)`);
        console.log(jsonStr);
        console.log('');
      }
      continue;
    }

    const headPath = join(FLEET_ROOT, product.headFile);
    if (!existsSync(headPath)) {
      fail++;
      console.error(`✗ ${product.id}: headFile missing on disk: ${product.headFile}`);
      continue;
    }

    const original = readFileSync(headPath, 'utf8');
    const result = injectJsonLdBlock(original, jsonStr);

    if (!result.changed) {
      ok++;
      console.log(`✓ ${product.id}: already up-to-date (${product.headFile})`);
      continue;
    }

    // Warn about pre-existing unmarked ld+json blocks (reconcile in pilot)
    const unmarked = countUnmarkedJsonLd(original);
    const warnUnmarked = unmarked > 0 ? ` [warn: ${unmarked} pre-existing unmarked ld+json]` : '';

    if (dryRun) {
      ok++;
      console.log(`✓ ${product.id}: would ${result.action} (${product.headFile})${warnUnmarked}`);
      console.log(jsonStr);
      console.log('');
      continue;
    }

    // Write
    writeFileSync(headPath, result.content, 'utf8');

    // Post-write safety checks
    const written = readFileSync(headPath, 'utf8');
    const safety = checkJsonLdSafety(original, written);

    if (safety.ok) {
      ok++;
      console.log(`✓ ${product.id}: ${result.action} (${product.headFile})${warnUnmarked}`);
    } else {
      // Restore original on failure
      writeFileSync(headPath, original, 'utf8');
      fail++;
      console.error(`✗ ${product.id}: safety check failed: ${safety.reason} — restored original`);
    }
  }

  console.log(
    `\nJSON-LD: ${ok} ok, ${fail} fail, ${skipped} no-headFile${dryRun ? ' (dry-run)' : ''}`
  );
  process.exit(fail === 0 ? 0 : 1);
}

const template = readFileSync(EDGE_TEMPLATE, 'utf8');

let written = 0;
let patched = 0;
let skipped = 0;

for (const product of registry.products) {
  if (onlyId && product.id !== onlyId) continue;
  const result = applyProduct(product);
  written += result.written;
  patched += result.patched;
  skipped += result.skipped;
  console.log(
    `${result.ok ? '✓' : '·'} ${product.id}: ${result.messages.join('; ')}`
  );
}

console.log(
  `\nDone. files=${written} workers_patched=${patched} skipped=${skipped}${dryRun ? ' (dry-run)' : ''}`
);

function applyProduct(product) {
  const messages = [];
  let writtenLocal = 0;
  let patchedLocal = 0;
  let skippedLocal = 0;
  let ok = true;

  const publicDirs = [product.publicDir, ...(product.altPublicDirs || [])]
    .filter(Boolean)
    .map((p) => join(FLEET_ROOT, p));

  const publicDir = publicDirs.find((d) => existsSync(dirname(d)) || existsSync(d));
  // prefer first listed if none exist — create it
  const targetPublic = publicDirs[0];
  if (!targetPublic) {
    return { ok: false, written: 0, patched: 0, skipped: 1, messages: ['no publicDir'] };
  }

  const surface = buildSurface(product);
  const llmsPath = join(targetPublic, 'llms.txt');
  const indexPath = join(targetPublic, 'index.md');
  const apiAiJsonPath = join(targetPublic, 'api-ai.json');
  const robotsPath = join(targetPublic, 'robots.txt');

  if (!existsSync(targetPublic)) {
    if (!dryRun) mkdirSync(targetPublic, { recursive: true });
    messages.push(`mkdir ${rel(targetPublic)}`);
  }

  if (!product.skipLlmsOverwrite && !product.hasDynamicLlms) {
    write(llmsPath, surface.llmsTxt);
    writtenLocal++;
    messages.push('llms.txt');
  } else {
    skippedLocal++;
    messages.push(product.hasDynamicLlms ? 'llms dynamic (skip file)' : 'llms skip overwrite');
  }

  write(indexPath, surface.indexMd);
  writtenLocal++;
  messages.push('index.md');

  // llms-full.txt — expanded dump for agents that prefer one-shot context
  const llmsFullPath = join(targetPublic, 'llms-full.txt');
  if (!product.skipLlmsOverwrite) {
    write(llmsFullPath, surface.llmsFullTxt);
    writtenLocal++;
    messages.push('llms-full.txt');
  }

  write(apiAiJsonPath, `${JSON.stringify(surface.catalog, null, 2)}\n`);
  writtenLocal++;
  messages.push('api-ai.json');

  // robots: ensure Sitemap + allow agent paths
  ensureRobots(robotsPath, product.url);
  messages.push('robots');

  // agent-edge.mjs next to worker when stack needs runtime
  if (product.worker) {
    const workerPath = join(FLEET_ROOT, product.worker);
    if (!existsSync(workerPath)) {
      messages.push(`worker missing: ${product.worker}`);
      ok = false;
    } else {
      const edgePath = join(dirname(workerPath), 'agent-edge.mjs');
      const edgeBody = template.replace(
        '__AGENT_SURFACE_JSON__',
        JSON.stringify(surface, null, 2)
      );
      write(edgePath, edgeBody);
      writtenLocal++;
      messages.push('agent-edge.mjs');

      const patch = patchWorker(workerPath);
      if (patch === 'patched') {
        patchedLocal++;
        messages.push('worker patched');
      } else if (patch === 'already') {
        messages.push('worker already wired');
      } else {
        messages.push(`worker patch: ${patch}`);
      }
    }
  }

  // wrangler run_worker_first for SPA Hono
  if (product.wrangler || product.stack === 'spa-hono') {
    const wranglers = [
      product.wrangler,
      product.id === 'anime-list' ? 'anime-list/wrangler.toml' : null,
      product.id === 'reader' ? 'reader/wrangler.toml' : null,
      product.id === 'email-manager' ? 'email-manager/wrangler.toml' : null,
      product.id === 'free-ai' ? 'free-ai/wrangler.toml' : null,
    ].filter(Boolean);
    for (const w of wranglers) {
      const p = join(FLEET_ROOT, w);
      if (existsSync(p)) {
        const r = patchWranglerRunWorkerFirst(p);
        messages.push(`wrangler:${r}`);
      }
    }
  }

  // Astro/static: _redirects for /api/ai → api-ai.json when no worker
  if (
    (product.stack === 'astro-static' || product.stack === 'next-static' || product.stack === 'spa-static') &&
    !product.worker
  ) {
    const redirects = join(targetPublic, '_redirects');
    const line = '/api/ai /api-ai.json 200\n';
    let existing = existsSync(redirects) ? readFileSync(redirects, 'utf8') : '';
    if (!existing.includes('/api/ai')) {
      write(redirects, existing + (existing.endsWith('\n') || !existing ? '' : '\n') + line);
      writtenLocal++;
      messages.push('_redirects /api/ai');
    }
  }

  // Never write a tiny public/sitemap.xml stub — it overrides Next.js
  // app/sitemap.ts and Astro @astrojs/sitemap generators. Huge sitemaps
  // must come from dynamic generators or dedicated content scripts.

  return {
    ok,
    written: writtenLocal,
    patched: patchedLocal,
    skipped: skippedLocal,
    messages,
  };

  function write(path, body) {
    if (dryRun) return;
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, body, 'utf8');
  }
}

function buildSurface(product) {
  const origin = product.url.replace(/\/$/, '');
  const productLinks = product.productLinks || [];
  const llmsLines = [
    `# ${product.name}`,
    '',
    `> ${product.summary}`,
    '',
    '## Product',
    '',
    ...productLinks.map(
      (l) =>
        `- [${l.title}](${l.url})${l.description ? `: ${l.description}` : ''}`
    ),
    '',
    '## Machine surfaces',
    '',
    `- [Agent catalog](${origin}/api/ai): JSON inventory of public surfaces`,
    `- [Homepage markdown](${origin}/index.md): Product brief without JS`,
    `- [This index](${origin}/llms.txt)`,
    '',
    '## Optional',
    '',
    '- [Foundry](https://sassmaker.com): Parent fleet showcase',
    '',
  ];

  const catalog = {
    name: product.name,
    version: '1',
    url: origin,
    llms: `${origin}/llms.txt`,
    llmsFull: `${origin}/llms-full.txt`,
    sitemap: `${origin}/sitemap.xml`,
    robots: `${origin}/robots.txt`,
    markdown: { suffix: '.md', negotiation: true },
    surfaces: [
      {
        id: 'home',
        url: `${origin}/`,
        md: `${origin}/index.md`,
        kind: product.stack?.startsWith('spa') ? 'spa' : 'static',
        description: 'Product home',
      },
      ...productLinks
        .filter((l) => !l.url.endsWith('/') || l.title !== 'Home')
        // Fleet hub (sassmaker) may list 20+ product roots; keep them all in /api/ai.
        .map((l) => ({
          id: slugId(l.title),
          url: l.url,
          md: null,
          kind: 'static',
          description: l.description || l.title,
        })),
    ],
    auth: {
      public: true,
      notes: 'Auth-walled app routes are not agent-indexed unless listed here.',
    },
  };

  const llmsFullTxt = [
    `# ${product.name} — full agent brief`,
    '',
    product.summary,
    '',
    '## Index',
    '',
    (product.indexMd || '').trim(),
    '',
    '## Product links',
    '',
    ...productLinks.map((l) => `- ${l.title}: ${l.url}${l.description ? ` — ${l.description}` : ''}`),
    '',
    '## Machine surfaces',
    '',
    `- ${origin}/llms.txt`,
    `- ${origin}/llms-full.txt`,
    `- ${origin}/api/ai`,
    `- ${origin}/index.md`,
    `- ${origin}/sitemap.xml`,
    `- ${origin}/robots.txt`,
    '',
    '## Contact / fleet',
    '',
    '- Fleet: https://sassmaker.com',
    '- Agent email for directory verification: sarthakagrawal@agentmail.to',
    '',
  ].join('\n');

  return {
    name: product.name,
    url: origin,
    llmsFullTxt,
    llmsTxt: llmsLines.join('\n'),
    indexMd: product.indexMd.endsWith('\n') ? product.indexMd : `${product.indexMd}\n`,
    catalog,
  };
}

function ensureRobots(robotsPath, siteUrl) {
  const origin = siteUrl.replace(/\/$/, '');
  let body = existsSync(robotsPath) ? readFileSync(robotsPath, 'utf8') : 'User-agent: *\nAllow: /\n';
  if (!/sitemap:/i.test(body)) {
    body = body.trimEnd() + `\n\nSitemap: ${origin}/sitemap.xml\n`;
  }
  if (!body.includes('/llms.txt')) {
    body =
      body.trimEnd() +
      `\n# Agent indexing\nAllow: /llms.txt\nAllow: /llms-full.txt\nAllow: /index.md\nAllow: /api/ai\n`;
  } else if (!body.includes('/llms-full.txt')) {
    body = body.trimEnd() + `\nAllow: /llms-full.txt\n`;
  }
  if (!dryRun) {
    mkdirSync(dirname(robotsPath), { recursive: true });
    writeFileSync(robotsPath, body.endsWith('\n') ? body : `${body}\n`, 'utf8');
  }
}

function patchWorker(workerPath) {
  let src = readFileSync(workerPath, 'utf8');
  if (src.includes(MARKER) || src.includes(IMPORT_MARKER)) return 'already';

  // Only auto-patch known OpenNext / simple fetch wrappers
  if (!src.includes('openNext') && !src.includes('export default')) {
    return 'unknown-shape';
  }

  // Insert import after the full import block (never mid multi-line `import {`).
  if (!src.includes("from './agent-edge.mjs'") && !src.includes('from "./agent-edge.mjs"')) {
    const importLine = "import { handleAgentEdge } from './agent-edge.mjs';\n";
    // Prefer end of last complete import (ends with `;`).
    const complete = [...src.matchAll(/^import[\s\S]*?;\s*$/gm)];
    if (complete.length) {
      const last = complete[complete.length - 1];
      const idx = last.index + last[0].length;
      src = src.slice(0, idx) + '\n' + importLine + src.slice(idx);
    } else {
      // Fallback: first non-comment/non-import line
      const m = src.match(/^(?:(?:\/\/[^\n]*|\/\*[\s\S]*?\*\/)\s*)*/);
      const start = m ? m[0].length : 0;
      src = src.slice(0, start) + importLine + src.slice(start);
    }
  }

  // Inject call at start of fetch handlers — several patterns
  const inject = `
    // Agent / LLM indexing surfaces (fleet GEO standard)
    {
      const agent = handleAgentEdge(request);
      if (agent) return agent;
    }
`;

  if (src.includes('async function fetch(request, env, ctx)')) {
    src = src.replace(
      /async function fetch\(request, env, ctx\) \{\s*\n/,
      (m) => m + inject
    );
  } else if (src.includes('async fetch(request, env, ctx)')) {
    src = src.replace(
      /async fetch\(request, env, ctx\) \{\s*\n/,
      (m) => m + inject
    );
  } else if (src.includes('fetch(request, env, ctx) {')) {
    src = src.replace(
      /fetch\(request, env, ctx\) \{\s*\n/,
      (m) => m + inject
    );
  } else {
    return 'no-fetch-hook';
  }

  if (!dryRun) writeFileSync(workerPath, src, 'utf8');
  return 'patched';
}

function patchWranglerRunWorkerFirst(wranglerPath) {
  let src = readFileSync(wranglerPath, 'utf8');
  const paths = [
    '/api/*',
    '/llms.txt',
    '/llms-full.txt',
    '/index.md',
    '/sitemap.xml',
  ];
  if (src.includes('run_worker_first')) {
    // ensure agent paths listed
    let changed = false;
    for (const p of paths) {
      if (!src.includes(`"${p}"`) && !src.includes(`'${p}'`)) {
        // try to inject into array
        src = src.replace(
          /run_worker_first\s*=\s*\[/,
          (m) => `${m}\n  "${p}",`
        );
        // toml style might use different format - skip if not
        changed = true;
      }
    }
    if (changed && !dryRun) writeFileSync(wranglerPath, src, 'utf8');
    return changed ? 'updated' : 'ok';
  }
  // JSONC assets block
  if (src.includes('"assets"') && src.includes('not_found_handling')) {
    src = src.replace(
      /"not_found_handling"\s*:\s*"[^"]+"/,
      (m) =>
        `${m},\n    "run_worker_first": ${JSON.stringify(paths)}`
    );
    if (!dryRun) writeFileSync(wranglerPath, src, 'utf8');
    return 'added';
  }
  return 'skip';
}

function slugId(title) {
  return String(title)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 40) || 'page';
}

function rel(p) {
  return p.replace(FLEET_ROOT + '/', '');
}

// --- JSON-LD generation ---

/**
 * Build a per-product JSON-LD @graph with the fleet Organization publisher
 * and a SoftwareApplication or WebSite node. Uses the shared productOrigin
 * preference chain from lib/registry.mjs.
 *
 * @param {object} product - registry product entry
 * @param {object} registry - full registry (for fleet publisher lookup)
 * @returns {{ '@context': string, '@graph': object[] }}
 */
function buildJsonLd(product, registry) {
  const origin = productOrigin(product);
  if (!origin) throw new Error(`Product ${product.id} has no url`);

  const schemaType = product.schemaType || 'SoftwareApplication';
  const productSameAs = Array.isArray(product.sameAs) ? product.sameAs : [];

  const orgNode = {
    '@type': 'Organization',
    '@id': FLEET_ORG_ID,
    name: FLEET_ORG_NAME,
    url: FLEET_HUB_URL,
    sameAs: [FLEET_HUB_URL, ...productSameAs],
  };

  const appNode = {
    '@type': schemaType,
    '@id': `${origin}/#app`,
    name: product.name,
    url: origin,
    description: product.summary,
    publisher: { '@id': FLEET_ORG_ID },
  };

  if (product.applicationCategory) {
    appNode.applicationCategory = product.applicationCategory;
  }
  if (product.offers) {
    appNode.offers = product.offers;
  }
  if (schemaType === 'WebSite' && product.url) {
    appNode.potentialAction = {
      '@type': 'SearchAction',
      target: `${origin}/?q={search_term_string}`,
      'query-input': 'required name=search_term_string',
    };
  }

  return {
    '@context': 'https://schema.org',
    '@graph': [orgNode, appNode],
  };
}

// --- JSON-LD injection helpers ---

/**
 * Inject or replace the marked JSON-LD block in an HTML/Astro head file.
 * Idempotent: if the current block already contains the same JSON, no change.
 *
 * @param {string} original - file content before injection
 * @param {string} jsonStr - JSON.stringify'd JSON-LD
 * @returns {{ changed: boolean, content: string, action: string }}
 */
function injectJsonLdBlock(original, jsonStr) {
  const block = `${JSONLD_START}\n<script type="application/ld+json">${jsonStr}</script>\n${JSONLD_END}`;

  // Check for existing marked block
  const startIdx = original.indexOf(JSONLD_START);
  const endIdx = original.indexOf(JSONLD_END);

  if (startIdx >= 0 && endIdx >= 0 && endIdx > startIdx) {
    // Replace existing block
    const before = original.slice(0, startIdx);
    const after = original.slice(endIdx + JSONLD_END.length);
    const content = before + block + after;

    // Idempotence: check if the existing block already has the same JSON
    const existingBlock = original.slice(startIdx, endIdx + JSONLD_END.length);
    if (existingBlock === block) {
      return { changed: false, content: original, action: 'already up-to-date' };
    }
    return { changed: true, content, action: 'replaced' };
  }

  // No existing marked block — insert before </head>
  const headCloseIdx = original.indexOf('</head>');
  if (headCloseIdx < 0) {
    return { changed: false, content: original, action: 'no </head> found' };
  }

  const before = original.slice(0, headCloseIdx);
  const after = original.slice(headCloseIdx);
  const content = before + block + '\n' + after;
  return { changed: true, content, action: 'injected' };
}

/**
 * Post-write safety checks:
 * 1. Re-extract the marked block and JSON.parse the script contents.
 * 2. </head> count in written file must match the original.
 *
 * @param {string} original - file content before write
 * @param {string} written - file content after write
 * @returns {{ ok: boolean, reason?: string }}
 */
function checkJsonLdSafety(original, written) {
  // 1. Re-extract the marked block and parse JSON
  const startIdx = written.indexOf(JSONLD_START);
  const endIdx = written.indexOf(JSONLD_END);
  if (startIdx < 0 || endIdx < 0 || endIdx <= startIdx) {
    return { ok: false, reason: 'marked block not found after write' };
  }
  const blockContent = written.slice(startIdx, endIdx);
  const scriptMatch = blockContent.match(
    /<script type="application\/ld\+json">([\s\S]*?)<\/script>/
  );
  if (!scriptMatch) {
    return { ok: false, reason: 'script tag not found inside marked block' };
  }
  try {
    JSON.parse(scriptMatch[1]);
  } catch (e) {
    return { ok: false, reason: `JSON.parse failed: ${e.message}` };
  }

  // 2. </head> count must match
  const originalHeadCount = (original.match(/<\/head>/g) || []).length;
  const writtenHeadCount = (written.match(/<\/head>/g) || []).length;
  if (originalHeadCount !== writtenHeadCount) {
    return {
      ok: false,
      reason: `</head> count changed: ${originalHeadCount} → ${writtenHeadCount}`,
    };
  }

  return { ok: true };
}

/**
 * Count application/ld+json script blocks that are NOT inside the marked
 * fleet-jsonld block. Used to warn about pre-existing hand-written blocks
 * that need reconciliation (pilot task).
 *
 * @param {string} content
 * @returns {number}
 */
function countUnmarkedJsonLd(content) {
  const allBlocks = (content.match(/application\/ld\+json/g) || []).length;
  // If there's a marked block, it contains 1 occurrence
  const hasMarked = content.includes(JSONLD_START) ? 1 : 0;
  return Math.max(0, allBlocks - hasMarked);
}
